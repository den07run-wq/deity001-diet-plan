-- ============================================
-- 饮食计划助手 - 数据库初始化脚本
-- ============================================

CREATE DATABASE IF NOT EXISTS diet_plan
  DEFAULT CHARACTER SET utf8mb4
  DEFAULT COLLATE utf8mb4_unicode_ci;

USE diet_plan;

-- ============================================
-- 1. 用户表
-- ============================================
CREATE TABLE IF NOT EXISTS users (
  id INT AUTO_INCREMENT PRIMARY KEY,
  username VARCHAR(50) NOT NULL UNIQUE,
  password_hash VARCHAR(255) NOT NULL,
  nickname VARCHAR(50),
  gender ENUM('男', '女'),
  age INT,
  height DECIMAL(5,2),
  weight DECIMAL(5,2),
  goal ENUM('减脂', '增肌', '保持健康', '其他') DEFAULT '保持健康',
  daily_calorie_target INT,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB;

-- ============================================
-- 2. 食材表
-- ============================================
CREATE TABLE IF NOT EXISTS food_items (
  id INT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  category ENUM('主食', '肉类', '蛋奶', '豆类', '蔬菜', '水果', '零食', '饮品', '调味料', '其他') NOT NULL,
  calories_per_100g DECIMAL(8,2) NOT NULL,
  protein_per_100g DECIMAL(6,2) DEFAULT 0,
  fat_per_100g DECIMAL(6,2) DEFAULT 0,
  carbs_per_100g DECIMAL(6,2) DEFAULT 0,
  fiber_per_100g DECIMAL(6,2) DEFAULT 0,
  unit VARCHAR(20) DEFAULT '克',
  is_custom TINYINT(1) DEFAULT 0,
  created_by INT,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE SET NULL
) ENGINE=InnoDB;

ALTER TABLE food_items ADD UNIQUE INDEX idx_name (name);

-- ============================================
-- 3. 饮食计划表
-- ============================================
CREATE TABLE IF NOT EXISTS diet_plans (
  id INT AUTO_INCREMENT PRIMARY KEY,
  user_id INT NOT NULL,
  name VARCHAR(100) NOT NULL,
  goal ENUM('减脂', '增肌', '保持健康', '其他') NOT NULL,
  start_date DATE NOT NULL,
  end_date DATE NOT NULL,
  daily_calories INT NOT NULL DEFAULT 2000,
  daily_protein DECIMAL(6,2) DEFAULT 0,
  daily_fat DECIMAL(6,2) DEFAULT 0,
  daily_carbs DECIMAL(6,2) DEFAULT 0,
  is_template TINYINT(1) DEFAULT 0,
  status ENUM('进行中', '已完成', '已取消') DEFAULT '进行中',
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
) ENGINE=InnoDB;

-- ============================================
-- 4. 计划食材关联表
-- ============================================
CREATE TABLE IF NOT EXISTS plan_foods (
  id INT AUTO_INCREMENT PRIMARY KEY,
  plan_id INT NOT NULL,
  food_id INT NOT NULL,
  day_of_week ENUM('周一', '周二', '周三', '周四', '周五', '周六', '周日') NOT NULL,
  meal_type ENUM('早餐', '午餐', '晚餐', '加餐') NOT NULL,
  quantity DECIMAL(8,2) NOT NULL,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (plan_id) REFERENCES diet_plans(id) ON DELETE CASCADE,
  FOREIGN KEY (food_id) REFERENCES food_items(id) ON DELETE CASCADE
) ENGINE=InnoDB;

-- ============================================
-- 5. 饮食记录表
-- ============================================
CREATE TABLE IF NOT EXISTS meal_records (
  id INT AUTO_INCREMENT PRIMARY KEY,
  user_id INT NOT NULL,
  plan_id INT,
  food_id INT NOT NULL,
  record_date DATE NOT NULL,
  meal_type ENUM('早餐', '午餐', '晚餐', '加餐') NOT NULL,
  quantity DECIMAL(8,2) NOT NULL,
  note VARCHAR(500),
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
  FOREIGN KEY (plan_id) REFERENCES diet_plans(id) ON DELETE SET NULL,
  FOREIGN KEY (food_id) REFERENCES food_items(id) ON DELETE CASCADE
) ENGINE=InnoDB;

